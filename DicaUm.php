            <h5>Como se comportar em uma entrevista de emprego</h5>

            <h6 style = "font-weight: bold;">1. Durma bem e alimente-se adequadamente </h6>

            <p>

                Para ter um bom desempenho, apresentar uma aparência saudável e manter sua concentração durante a entrevista, é importante que você esteja descansada e bem alimentada.
                Durma bem na véspera e evite alimentos que possam te causar algum tipo de desconforto ou de não estar suficientemente alimentada.

            </p>  

            <h6 style = "font-weight: bold;">2. Seja pontual </h6>

            <p>

                Veja com antecedência qual o melhor percurso até o local da entrevista e quanto tempo em média é necessário para chegar lá.
                Programe-se para chegar com pelo menos 20 ou 15 minutos de antecedência. Se durante o percurso acontecer um imprevisto e você perceber que se atrasará,
                entre em contato com o entrevistador e informe quanto tempo você prevê que chegará atrasada. 
                Esta dica também vale para as entrevistas online. Entre no dispositivo que será usado para a entrevista com antecedência de uns 30 minutos,
                faça os testes de áudio e vídeo para ter certeza que está tudo certo e caso perceba que a conexão de sua internet não está boa o suficiente para uma ligação por áudio e vídeo,
                avise com antecedência e tente remarcar a conversa para um outro momento.

            </p>  

            <h6 style = "font-weight: bold;">3. Vista-se de maneira adequada</h6>

            <p>

                Evite roupas extremamente informais ou extravagantes (leia como se vestir aqui). Já recebi candidatas(os) de chinelo, boné, roupas muito curtas ou decotada e etc.
                Procure usar roupas que demonstre uma certa formalidade. Em relação a essa dica já vi gente reclamando que não tem dinheiro para comprar uma roupa cara para a 
                entrevista. Não se trata de usar uma roupa cara, não é isso que o entrevistador vai avaliar,
                você deve apenas buscar no seu guarda-roupa aquela roupa que demonstre certa formalidade e te deixe confortável.
                Evite usar tênis ou qualquer outro tipo de calçado muito informal. Na dúvida de como se vestir, procure saber como os funcionários da empresa que fará a 
                entrevista se vestem para trabalhar e se vista de forma compatível.
                O que tem que chamar atenção na entrevista é você com suas qualidades e não sua roupa.
                Essa dica também se aplica a entrevista online. Não é porque você está na sua casa que você pode aparecer de pijama ou sem camisa (no caso dos homens) ou com o cabelo despenteado.
                Certa vez uma colega minha foi entrevistar um candidato que apareceu no webcam deitado na cama e sem camisa. Segundo ele, a cidade que ele morava era muito quente.
                Ela se sentiu tão constrangida com a situação que resolveu terminar a entrevista em pouquíssimos minutos.

            </p>  

            <h6 style = "font-weight: bold;">4. Comporte-se adequadamente desde a recepção</h6>

            <p>

                Ao entrar na empresa você já estará sendo observado pelas pessoas que interagirem com você. 
                Por isso, para saber como se comportar em uma entrevista de emprego de forma assertiva cumprimente as pessoas com educação,
                sente-se confortavelmente na cadeira ou sofá (não precisa e nem deve ficar rígida como um robô, nem super a vontade como se estivesse na sala da sua casa)
                e não seja inconveniente com a(s) recepcionista(s) ou qualquer outro funcionário que te recepcionar antes do 
                entrevistador fazendo brincadeirinhas ou comentários inadequados para aquele momento.

            </p>  

            <h6 style = "font-weight: bold;">5. Comunique-se de maneira formal/profissional</h6>

            <p>

                Mesmo que o(a) entrevistador(a) seja bem simpático(a) e faça você se sentir bem a vontade, lembre-se que você ainda está numa situação profissional e deve se comportar de forma compatível.
                Alguns ex-colegas de trabalho já disseram que quando fizeram entrevista comigo se sentiram tão a vontade que parecia estar conversando com uma amiga.
                Isso é ótimo, pois quando nos sentimos mais confortáveis podemos mostrar melhor nosso desempenho, mas tome muito cuidado para não confundir simpatia ou até mesmo certa informalidade (algumas empresas preferem ter ambiente informal mesmo) com intimidade.
                Você não tem intimidade com o entrevistador, e mesmo que tenha, aquela é uma situação profissional que exige que você se comporte de maneira adequada. Nada de falar palavrão durante a entrevista ou usar gírias muito informais ou chamar o entrevistador por algum apelido. Por incrível que pareça,
                já teve um candidato que me chamou de “Tata”, continuou a conversa fazendo umas perguntas e comentários muito inconvenientes sobre a empresa. Nem preciso dizer que ele foi desclassificado assim que relatei o ocorrido para o gestor que faria a próxima etapa do processo.
                Quer saber o que os astros dizem sobre sua vida profissional e carreira? 

            </p>  

            <h6 style = "font-weight: bold;">6. Leve uma cópia do currículo e caneta</h6>

            <p>
                
                Como se comportar em uma entrevista de emprego e surpreender? Se antecipe! Leve uma cópia do CV para o caso
                do entrevistador esquecer-se de imprimir. Sim, os entrevistadores também são humanos e comentem falhas. 
                Caso você não queira imprimir o CV desnecessariamente, pergunte antes da entrevista se é necessário levar 
                seu CV impresso. Lembre-se de levar uma caneta para preencher possíveis formulários.

            </p>     

            <h6 style = "font-weight: bold;">7. Domine seu CV</h6>

            <p>

                Já entrevistei alguns candidatos que pareciam confusos sobre seu histórico profissional ou que me respondiam
                algo diferente do que estava registrado no CV. Antes da entrevista leia e releia seu currículo e anote
                para você quais foram os principais aprendizados e resultados adquiridos em cada uma das suas experiências profissionais. 
                Conheça sua história de trás para frente e de frente para trás. Quando um candidato apresenta um comportamento confuso em relação a 
                sua história profissional e/ou formações acadêmicas, isso pode fazer o entrevistador imaginar que o candidato está mentindo.

            </p>     


            <h6 style = "font-weight: bold;">8. Olhe o entrevistador nos olhos</h6>

            <p>

                Você demonstrará mais confiança se olhar o entrevistador nos olhos. Não precisa ficar olhando 
                fixamente nos olhos nem o tempo todo, mas pelo menos demonstre que você está olhando no rosto da pessoa 
                enquanto fala. Se você se sentir desconfortável de olhar muito tempo nos olhos, procure olhar para o 
                nariz, na direção entre os olhos ou um pouco acima, entre as sobrancelhas. Também é possível alternar o olhar entre os olhos 
                e a boca enquanto a pessoa fala. Assim você demonstrará que está atento ao que é dito.

            </p>

            <h6 style = "font-weight: bold;">9. Desligue o celular</h6>

            <p>
                
                Nunca atenda o celular durante uma entrevista, a menos que você esteja passando por alguma situação
                emergencial (tipo ter um parente hospitalizado) e no início da entrevista você avise que se receber uma ligação
                sobre este assunto terá que atender. Se você esquecer de desligar o celular ou colocar em
                modo silencioso e ele tocar durante a entrevista, aja com naturalidade pedindo desculpas e desligue-o em seguida.

            </p>

            <h6 style = "font-weight: bold;">10. Lide bem com a ansiedade e demonstre confiança</h6>

            <p>
                
                Na minha experiência, já vi muito candidato ser preferido para a contratação por demonstrar mais
                confiança durante a entrevista do que outros com mais experiência e qualificação. Desta forma, use suas melhores estratégias
                para lidar melhor com a ansiedade e demonstrar confiança durante a entrevista.
                Esqueça essa estória de controlar totalmente a ansiedade ou achar que conseguirá não ficar ansioso durante a entrevista. 
                O que você precisa é aprender a conviver bem com ela e não deixá-la te atrapalhar. Se você não tem a menor ideia de como fazer 
                isso e ainda por cima demonstrar confiança, veja este vídeo até o fim que tem 7 dicas muito boas sobre este assunto, principalmente a última.

            </p>

            <h6 style = "font-weight: bold;">11. Seja objetivo sem ser muito resumido</h6>

            <p>

                Esta dica é para aquelas pessoas que gostam de falar muito, principalmente quando ficam
                ansiosas e acabam se perdendo em relação ao objetivo da resposta. Preste bastante atenção à pergunta
                e responda exatamente o que está sendo perguntado, sem se alongar muito na resposta ou incluindo informações 
                muito detalhadas. Se o entrevistador quiser saber mais, ele irá te perguntar com certeza.

            </p>

            <h6 style = "font-weight: bold;">12. Nunca minta!!!</h6>

            <p>

                Os candidatos prolixos (que fala ou escreve usando mais palavras do que o necessário, que não consegue resumir uma ideia ou encurtar um pensamento)
                são vistos com certa desconfiança, pois parece que são pessoas que querem enrolar o entrevistador ou mentir durante a entrevista, 
                pois este é um dos sinais de candidatos que estão mentindo na entrevista, dar respostas muito longas, 
                com vários detalhes, no fim a explicação fica confusa e o entrevistador não entende claramente os pontos principais da explicação.
                E por falar em mentira, NUNCA MINTA! Os entrevistadores estão tão acostumados a fazer entrevista que conseguem identificar quando um candidato 
                está mentindo. Todos nós damos sinais quando mentimos e é muito difícil controlar estes sinais. Um comportamento comum de quem está mentindo 
                é ficar mais agitado ou desconfortável quando está falando algo que não devia. Então nem insista!
                O que costumo orientar meus clientes a fazer é omitir as informações mais delicadas da situação que eles não querem falar e procurar reforçar
                os pontos positivos e importantes do ocorrido. Isso fará com que eles falem com mais confiança e convicção sobre situações embaraçosas
                como, por exemplo, o motivo de ter saído ou sido demitido de alguma experiência anterior. Toda estória pode ser olhada por mais de uma perspectiva
                e é isto que você deve buscar para contar algo que seja verdadeiro e menos embaraçoso.


            </p>

            <h6 style = "font-weight: bold;">13. Fale com convicção de suas conquistas</h6>

            <p>

                Já vi candidatos muito bons, com uma experiência muito interessante e que apresentavam uma modéstia excessiva ao falar
                de suas principais conquistas. Você não precisa ser arrogante ou esnobe ao falar sobre sua carreira, mas também não precisa
                se comportar como se nada do que você fala fosse significativo. Essa é uma das dicas de ouro sobre como se comportar em uma entrevista de emprego.
                Você tem que ser a primeira pessoa a dar valor a sua experiência, seja ela qual for, inclusive para quem está buscando o
                primeiro emprego. Nestes casos procure falar de forma positiva de suas conquistas na escola, cursos ou trabalhos voluntários.
                Procure demonstrar apreço por sua experiência profissional enquanto responde as perguntas e reforce as experiências e fatos positivos. 

            </p>

            <h6 style = "font-weight: bold;">14. Evite “queimar seu filme” criticando ou menosprezando alguma de suas experiências anteriores</h6>

            <p>

                Pior do que a situação anterior, em relação a como se comportar em uma entrevista de emprego, são
                os candidatos que se sentem tão frustrados com a empresa atual ou alguma anterior e usa o momento da entrevista para desabafar e/ou criticar sua experiência lá.
                Cuidado ao criticar a empresa por não ter a oportunidade de fazer tudo o que era esperado ou
                porque a empresa nunca apoiava a implementação de novos projetos e etc. Se a(s) empresa(s) era(m) ruim(ns) e sua experiência
                lá foi ruim, pode acabar passando a impressão de que o que está relatado no seu CV é mais do que aconteceu de fato na empresa.

            </p>

            <h6 style = "font-weight: bold;">15. Responda sobre seus pontos fortes/fracos ou qualidades/defeitos de maneira adequada</h6>

            <p>

                Esta é a pergunta mais comum nas entrevistas e a que os candidatos têm mais dificuldade em
                responder. Para respondê-la com mais facilidade e segurança, você precisa preparar a resposta desta pergunta
                antes da entrevista. Pegue papel e caneta e anote pelo menos 5 de suas qualidades que poderiam ser aproveitadas
                no cargo que você deseja ser contratado e faça uma pequena explicação sobre cada uma destas qualidades e anote
                alguns exemplo de quando elas foram necessárias na sua carreira.
                Quando terminar, anote pelo menos 5 pontos fracos ou defeitos que você tem buscado melhorar. 
                Atenção!!! Busque falar de defeitos (ou pontos fracos) que não te prejudiquem diretamente na execução
                das tarefas do cargo. Por exemplo, se a vaga não exige ter inglês avançado, você pode dizer que o seu nível de inglês
                ainda não está bom o suficiente e o que você tem feito para melhorar isso.
                Evite dizer que é perfeccionista ou ansiosa, estas respostas já são muito clichê e o pior é que muitas vezes o candidato
                nem consegue explicar como a ansiedade ou o perfeccionismo tem atrapalhado seu desenvolvimento de carreira.
                Na entrevista você não precisará responder usando todas as 5 qualidades ou defeitos, mas a ideia é você estar mais
                preparada, com mais respostas para dar do que o necessário, para o caso de se esquecer de alguma qualidade/defeito durante a entrevista de emprego.

            </p>

            <h6 style = "font-weight: bold;">16. Prepare-se para outras perguntas embaraçosas</h6>

            <p>

                Não bastasse a pergunta sobre qualidades e defeitos que já deixa os candidatos
                bem desconfortáveis, existem várias outras que também podem ser bastante embaraçosas. 
                Como por exemplo, como era a sua relação com algum antigo chefe que você sabe que era ruim, ou o motivo
                de você ter pedido demissão ou ter sido demitido, ou qualquer outra situação delicada ao longo de sua estória
                profissional ou até mesmo perguntas que parecem estranhas, por exemplo “O que você faria
                se fosse o único sobrevivente de um acidente de avião?”.
                Neste último caso, é um pouco mais difícil se preparar para elas, pois são perguntas
                para testar sua criatividade na resolução de problemas, capacidade de inovação, raciocínio
                lógico ou até mesmo para ver como você reage diante de uma situação inesperada. Porém, em relação às perguntas
                sobre sua carreira, você tem como se preparar para respondê-las. Antes da entrevista relembre todos os fatos que podem
                ser um pouco obscuros na sua trajetória e que você não gostaria de contar, escreva uma breve resposta para cada um usando as dicas
                que dei no tópico 12 (nunca minta!!!), ensaie essa resposta em voz alta até se sentir confortável em responder
                desta forma e faça alguns ajustes na resposta se for necessário.
                Você não precisa levar os rumos da entrevista para essas situações embaraçosas, mas caso alguém te pergunte sobre
                algo que não queira muito falar, você já se sentirá mais confortável em dar uma resposta e aprender a como se comportar em uma entrevista de emprego. 

            </p>

            <h6 style = "font-weight: bold;">17. Prepare-se para dar respostas por competências</h6>

            <p>

                Entrevistas por competências tem se tornado cada vez mais frequentes para as empresas, pois elas desejam
                descobrir o que você sabe teoricamente, o que sabe fazer na prática e o que de fato você quer fazer. Para descobrir uma
                determinada competência como, por exemplo, negociação é feita uma pergunta ou pedido para que você conte sobre a negociação
                mais importante que fez em sua carreira, ou na última experiência e que trouxe o melhor resultado.
                Neste tipo de pergunta, você terá que contar de alguma experiência real que você tenha de fato vivenciado e não criar
                algo que você gostaria de fazer. Para isto, é importante você ter mapeado as principais competências requeridas
                para a posição que você está concorrendo seguindo o passo a passo abaixo:<br>
                •	Revise seu CV e anote as principais tarefas realizadas em cada experiência;<br>
                •	Anote quais competências foram requeridas para realizar cada tarefa. Por exemplo: trabalho em equipe, liderança, iniciativa, criatividade e etc;<br>
                •	Ou faça uma lista de todas as competências que você possui e descreva brevemente uma situação onde cada uma foi usada;<br>
                •	Anote as competências que você está desenvolvendo e como.

            </p>    

            <h6 style = "font-weight: bold;">18. Faça perguntas pertinentes ao fim da entrevista</h6>

            <p>

                Geralmente no final da entrevista o entrevistador pergunta se você tem mais alguma dúvida
                ou pergunta para fazer. Aproveite este momento para fazer perguntas pertinentes sobre a empresa
                ou o cargo. Pergunte sobre detalhes interessantes que você pesquisou sobre a empresa e gostaria 
                de saber mais. Assim você demonstra que está interessada na empresa e na vaga em questão já que está buscando
                maiores informações.

            </p>    

            <h6 style = "font-weight: bold;">19. Verifique outros detalhes antes da entrevista online</h6>

            <p>

                Além do que já foi mencionando anteriormente sobre como se comportar em uma entrevista
                de emprego pela internet, fique atento a alguns detalhes que podem comprometer a sua entrevista:<br>
                •	Antes do dia da entrevista, veja se a conexão da sua casa ou lugar onde você estiver no dia da entrevista
                    é boa o suficiente para uma transmissão de áudio e vídeo. Caso não seja, vá para um lugar com conexão estável e melhor.<br>
                •	Faça sua entrevista num horário e local no qual você não seja interrompida. Avise as pessoas que
                    moram com você que você não poderá ser interrompida durante a entrevista.<br>
                •	Mantenha o ambiente atrás da você organizado. Tome cuidado para não fazer a entrevista apontando a câmera
                    para um armário aberto e bagunçado, ou uma cama cheia de coisas em cima. Pior ainda se você tentar dizer que uma de
                    suas qualidades é organização (!?) e o entrevistador perceber algo completamente diferente. Não precisa ter um belo 
                    cenário atrás de você, uma parede é suficiente.

            </p>        

            <h6 style = "font-weight: bold;">20. Seja sua melhor versão autêntica</h6>

            <p>

                Para finalizar, espero que tenha ficado claro ao longo do texto que a melhor forma de saber como
                se comportar em uma entrevista de emprego é ser a sua melhor versão autêntica, ou seja, é para demonstrar
                o melhor de quem você é de verdade e não uma interpretação de alguém que você não é ou que gostaria de ser.
                Ao demonstrar como é sua melhor versão, você deve evitar aqueles comportamentos que citei que não são convenientes
                e acabam sendo mal interpretados pelo entrevistador, mas jamais tente parecer um robô ou um personagem. Existem
                estudos que demonstram que quando somos autênticos, passamos uma imagem de maior confiança para quem nos avalia. E acredite,
                além de querer entender mais sobre suas competências que estão descritas no CV, o entrevistador quer saber se você passa
                a impressão de ser uma pessoa confiante e confiável.
                Mais uma coisa que você deve lembrar-se é que a entrevista é como um visto de entrada de um país no seu
                passaporte. Ela te permite acesso até o país (empresa), mas não garante a sua entrada. Mesmo que você tenha feito
                o seu melhor na entrevista, pode ser que você não seja contratada por diversos motivos, mas principalmente pela falta
                de alinhamento entre as suas expectativas com as da empresa e as da empresa em relação a você 
                Ao relembrá-la sobre isso, não quero te fazer se sentir desanimada ao fim deste texto, mas apenas relembrar que essa
                possibilidade existe e que você não deve ir para a entrevista criando altas expectativas sobre sua aprovação. Apenas vá e faça o seu
                melhor independente de qual resultado poderá ter deste momento.

            </p>

