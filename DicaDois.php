            São diversos os direitos e deveres do trabalhador. Em relação aos direitos, alguns são de extrema importância, pois garantem outros direitos, como os direitos previdenciários. A carteira de trabalho (CTPS), por exemplo, deve ser assinada pelo empregador desde o primeiro dia de trabalho e com todos os dados preenchidos corretamente. A carteira de trabalho permite que o trabalhador contribua com a previdência social e tenha direito à aposentadoria posteriormente.<br><br>
Ao contratar o empregado, a empresa também deve providenciar o exame médico para avaliar a saúde do trabalhador. Ao fim do contrato de trabalho, um exame médico de demissão também deve ser realizado.<br><br>
Outro direito é receber o salário até o quinto dia útil do mês seguinte ao mês trabalhado. Assim, um trabalhador deve receber seu salário do mês de março, por exemplo, até o quinto dia útil do mês de abril. Atrasos no pagamento devem ser evitados ao máximo, pois podem levar a processos trabalhistas.<br><br>
Ainda em relação ao pagamento, todo trabalhador tem direito a um dia de descanso semanal e este deve ser remunerado. Ou seja, o fato de o trabalhador descansar no domingo não deve levar o empregador a descontar esse dia do seu salário. Além disso, é importante saber que o descanso pode ocorrer em outros dias da semana, embora o domingo seja o mais comum.<br><br>
Essas são os principais direitos do trabalhador e que devem ser respeitados sempre, para evitar que outros direitos sejam comprometidos. O trabalhador deve estar sempre atento, pois caso esses direitos sejam desrespeitados, ele deverá buscar auxílio por meios jurídicos.<br><br>

                

