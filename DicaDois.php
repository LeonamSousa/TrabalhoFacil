            <h6 style = "font-weight: bold;">Abono Salarial: Como funciona?</h6>

            <p>

            Entre os diversos benefícios trabalhistas estabelecidos pela legislação brasileira, o abono salarial é um dos mais importantes. Esse benefício auxilia diversas pessoas no país, aumentando a renda salarial anualmente. É muito importante não apenas saber quem tem direito ao benefício, mas como receber e como evitar perder as datas de pagamento.<br><br>

            </p>  

            <h6 style = "font-weight: bold;">O que é?</h6>

            <p>
                
            O abono salarial é um benefício estabelecido na legislação trabalhista e na Constituição Federal do Brasil. Ele prevê o pagamento de um salário por ano aos trabalhadores que recebem até 2 salários mínimos por mês.<br><br>
O valor do abono salarial é de um salário mínimo, de acordo com o valor estabelecido pelo governo no ano vigente. Assim, sempre que o valor do salário mínimo for alterado, o valor a ser pago pelo abono salarial será alterado também.<br><br>


            </p>     

            <h6 style = "font-weight: bold;">Quem pode receber?</h6>

            <p>

            Nem todos os trabalhadores que recebem até 2 salários mínimos em média por mês podem receber esse benefício. Para ter direito, o trabalhador deve seguir outros requisitos estabelecidos por lei. Primeiro, o trabalhador deve estar cadastrado no sistema do Programa de Integração Social (PIS) ou no do Programa de Formação do Patrimônio de Servidor Público (PSEP) há no mínimo 5 anos. Essa inscrição é realizada quando o trabalhador solicita a carteira de trabalho.<br><br>
Em segundo lugar, o trabalhador deve ter trabalhado com carteira assinada por no mínimo 30 dias no ano anterior. Não é necessário que esses 30 dias tenham sido trabalhados na mesma empresa. Outro requisito é que o empregador entregue corretamente as informações dos empregadores para a Relação Anual de Informações Sociais (RAIS).<br><br>
Para saber se tem direito ou não ao abono salarial, basta acessar o site do Ministério do Trabalho e Emprego (MTE) e buscar pelo sistema do abono salarial. Depois, é necessário informar o número de CPF ou o número do PIS/PASEP, e a data de nascimento. Ao fim, será informado se possui direito ou não ao benefício. Se possui, também aparecerá o valor a ser recebido.<br><br>
          
            </p>     


            <h6 style = "font-weight: bold;">Como receber?</h6>

            <p>

            O pagamento do abono salarial é realizado conforme um calendário criado em conjunto pelo MTE e pela Caixa Econômica Federal. Esse calendário estabelece as datas de pagamentos do benefício no ano, organizadas de acordo com a data de nascimento dos trabalhadores. O calendário fica disponível no site do MTE e da Caixa Econômica Federal.<br><br>
Assim, o calendário prevê que para os nascidos em determinado mês, a retirada do benefício deve ser realizada de determinado dia até tal dia. Dessa forma, as agências bancárias não ficam sobrecarregadas e nem o orçamento da União.<br><br>
No caso dos trabalhadores cadastrados no sistema do PIS, é possível receber o benefício nas agências da Caixa Econômica Federal. Para isso, é apenas necessário apresentar o número do PIS e um documento de identificação. Caso o indivíduo possua o Cartão Cidadão, é possível retirar o valor do abono salarial em caixas eletrônicos e em casas lotéricas.<br><br>
Já para os trabalhadores cadastrados no sistema do PASEP, é necessário dirigir-se a uma agência do Banco do Brasil. Basta que o trabalhador apresente um documento de identificação e o valor já será depositado em forma de crédito na sua conta. Também é possível receber o valor diretamente no caixa do banco. Essa escolha apenas depende do trabalhador.<br><br>

            </p>

